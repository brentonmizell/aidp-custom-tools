_COMMON_PREAMBLE = '''"""{desc}

Generated by AIDP Tool Wizard.
"""

from typing import Any, Dict

from aidputils.agents.tools.custom_tools.base import CustomToolBase
from .utils.config_utils import get_cfg, ok, fail

try:
    from aidp_debug import debug, debug_warn, debug_error, DebugLog
except ImportError:
    def debug(*a, **k): pass
    def debug_warn(*a, **k): pass
    def debug_error(*a, **k): pass
    class DebugLog:
        @staticmethod
        def embed(r): return r
'''


_FILE_READER_TEMPLATE = _COMMON_PREAMBLE + '''

@CustomToolBase.register
class {cls}(CustomToolBase):
    """Read a file from the AIDP volume the wizard pre-configured."""

    @classmethod
    def _execute_tool(cls, conf: Dict[str, Any], runtime_params: Dict[str, Any],
                      **context_vars) -> Dict[str, Any]:
        debug("{cls}._execute_tool start")
        try:
            from .utils.aidp_io import read_text
            path = runtime_params.get("path", "")
            if not path:
                return fail("`path` is required", "ValidationError")
            volume_key = get_cfg(conf, "volume_key", "")
            if not volume_key:
                return fail("conf.volume_key not set — re-run the wizard or set manually",
                            "ValidationError")
            uri = f"master:{{volume_key}}:{{path}}"
            content = read_text(uri, conf, context_vars)
            return DebugLog.embed(ok({{"path": path, "content": content,
                                        "bytes": len(content.encode('utf-8'))}}))
        except Exception as e:
            return DebugLog.embed(fail(str(e), type(e).__name__))
'''


_SQL_TEMPLATE = _COMMON_PREAMBLE + '''

@CustomToolBase.register
class {cls}(CustomToolBase):
    """Run a parameterized SQL query against the catalog the wizard pre-configured.
    Note: For non-trivial SQL, prefer AIDP's first-party SQL tool. This is a
    minimal scaffold for tools that wrap one specific query."""

    @classmethod
    def _execute_tool(cls, conf: Dict[str, Any], runtime_params: Dict[str, Any],
                      **context_vars) -> Dict[str, Any]:
        debug("{cls}._execute_tool start")
        try:
            from .utils.aidp_io import get_external_catalog_connection
            query = runtime_params.get("query", "")
            if not query:
                return fail("`query` is required", "ValidationError")
            conn_info = get_external_catalog_connection(
                get_cfg(conf, "catalog_key", "") or get_cfg(conf, "catalog", ""),
                conf, context_vars,
            )
            # NOTE: implementation of the query execution depends on
            # whether the catalog is Oracle / Spark — wire up oracledb or
            # pyspark here based on conn_info['type'].
            return DebugLog.embed(ok({{"connection": conn_info["type"],
                                        "query": query,
                                        "TODO": "wire up oracledb cursor here"}}))
        except Exception as e:
            return DebugLog.embed(fail(str(e), type(e).__name__))
'''


_REST_TEMPLATE = _COMMON_PREAMBLE + '''
import requests


@CustomToolBase.register
class {cls}(CustomToolBase):
    """Call an external REST endpoint."""

    @classmethod
    def _execute_tool(cls, conf: Dict[str, Any], runtime_params: Dict[str, Any],
                      **context_vars) -> Dict[str, Any]:
        debug("{cls}._execute_tool start")
        try:
            method = (runtime_params.get("method") or "GET").upper()
            url = runtime_params.get("url", "")
            body = runtime_params.get("body", "")
            timeout = get_cfg(conf, "timeout", 30)
            if not url:
                return fail("`url` is required", "ValidationError")
            kwargs = {{"timeout": timeout}}
            if body:
                kwargs["data"] = body
            r = requests.request(method, url, **kwargs)
            r.raise_for_status()
            ct = r.headers.get("Content-Type", "")
            payload = r.json() if "json" in ct else r.text[:64000]
            return DebugLog.embed(ok({{"status": r.status_code, "body": payload}}))
        except Exception as e:
            return DebugLog.embed(fail(str(e), type(e).__name__))
'''


_RAG_TEMPLATE = _COMMON_PREAMBLE + '''

@CustomToolBase.register
class {cls}(CustomToolBase):
    """Search an AIDP Knowledge Base."""

    @classmethod
    def _execute_tool(cls, conf: Dict[str, Any], runtime_params: Dict[str, Any],
                      **context_vars) -> Dict[str, Any]:
        debug("{cls}._execute_tool start")
        try:
            from .utils.aidp_kb import kb_search
            query = runtime_params.get("query", "")
            top_k = int(runtime_params.get("top_k", 5))
            kb_key = get_cfg(conf, "kb_key", "")
            if not query or not kb_key:
                return fail("`query` and conf.kb_key are both required",
                            "ValidationError")
            hits = kb_search(query, kb_key, conf, top_k=top_k,
                             context_vars=context_vars)
            return DebugLog.embed(ok({{"query": query, "top_k": top_k,
                                        "hits": hits, "count": len(hits)}}))
        except Exception as e:
            return DebugLog.embed(fail(str(e), type(e).__name__))
'''


_ECHO_STUB = _COMMON_PREAMBLE + '''

@CustomToolBase.register
class {cls}(CustomToolBase):
    """Stub generated when no LLM was used. Replace _execute_tool with your logic."""

    @classmethod
    def _execute_tool(cls, conf: Dict[str, Any], runtime_params: Dict[str, Any],
                      **context_vars) -> Dict[str, Any]:
        debug("{cls}._execute_tool start")
        try:
            payload = runtime_params.get("input", "")
            return DebugLog.embed(ok({{"echo": payload}}))
        except Exception as e:
            return DebugLog.embed(fail(str(e), type(e).__name__))
'''
